package com.apl.otps.lara.entities;


/**
 * @author naresh.thota
 *
 */

public class ParamDetails {
	
	private String paramIdentifier;
	private String flag;
	
	
	/**
	 * @return the paramIdentifier
	 */
	public String getParamIdentifier() {
		return paramIdentifier;
	}
	/**
	 * @param paramIdentifier the paramIdentifier to set
	 */
	public void setParamIdentifier(String paramIdentifier) {
		this.paramIdentifier = paramIdentifier;
	}
	/**
	 * @return the flag
	 */
	public String getFlag() {
		return flag;
	}
	/**
	 * @param flag the flag to set
	 */
	public void setFlag(String flag) {
		this.flag = flag;
	}
	
	

}
